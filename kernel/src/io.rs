const CHAR_WIDTH: usize = 8;
const CHAR_HEIGHT: usize = 8;
const SCREEN_COLS: usize = 80;
const SCREEN_ROWS: usize = 25;

fn pixel_color(fg: u8, bg: u8, fmt: PixelFormat) -> (u8, u8, u8) {
    // Simple 16-color VGA palette mapping
    let fg_rgb = vga_color(fg);
    let bg_rgb = vga_color(bg);
    match fmt {
        PixelFormat::Rgb => (fg_rgb.0, fg_rgb.1, fg_rgb.2),
        PixelFormat::Bgr => (fg_rgb.2, fg_rgb.1, fg_rgb.0),
        _ => (fg_rgb.0, fg_rgb.1, fg_rgb.2),
    }
}

fn vga_color(c: u8) -> (u8, u8, u8) {
    match c & 0x0F {
        0x0 => (0, 0, 0),
        0x1 => (0, 0, 170),
        0x2 => (0, 170, 0),
        0x3 => (0, 170, 170),
        0x4 => (170, 0, 0),
        0x5 => (170, 0, 170),
        0x6 => (170, 85, 0),
        0x7 => (170, 170, 170),
        0x8 => (85, 85, 85),
        0x9 => (85, 85, 255),
        0xA => (85, 255, 85),
        0xB => (85, 255, 255),
        0xC => (255, 85, 85),
        0xD => (255, 85, 255),
        0xE => (255, 255, 85),
        0xF => (255, 255, 255),
        _ => (255, 255, 255),
    }
}

fn draw_pixel(fb: &mut FramebufferState, x: usize, y: usize, r: u8, g: u8, b: u8) {
    if x >= fb.width || y >= fb.height { return; }
    let bpp = fb.bytes_per_pixel;
    let offset = y * fb.stride * bpp + x * bpp;
    unsafe {
        let ptr = (fb.addr + offset as u64) as *mut u8;
        *ptr.add(0) = b;
        *ptr.add(1) = g;
        *ptr.add(2) = r;
    }
}

fn draw_char(fb: &mut FramebufferState, c: u8, row: usize, col: usize) {
    if fb.addr == 0 { return; }
    let (r, g, b) = pixel_color(fb.fg, fb.bg, fb.pixel_format);
    let glyph_idx = if c >= 32 && c <= 126 { (c - 32) as usize } else { 0 };
    let glyph = &FONT[glyph_idx * 8..glyph_idx * 8 + 8];
    let base_x = col * CHAR_WIDTH;
    let base_y = row * CHAR_HEIGHT;
    for dy in 0..CHAR_HEIGHT {
        let row_bits = glyph[dy];
        for dx in 0..CHAR_WIDTH {
            if (row_bits >> (7 - dx)) & 1 != 0 {
                draw_pixel(fb, base_x + dx, base_y + dy, r, g, b);
            } else {
                draw_pixel(fb, base_x + dx, base_y + dy, vga_color(fb.bg).0, vga_color(fb.bg).1, vga_color(fb.bg).2);
            }
        }
    }
}

pub fn init_framebuffer(addr: u64, width: usize, height: usize, bpp: usize, stride: usize, fmt: PixelFormat) {
    let mut fb = FRAMEBUFFER.lock();
    fb.addr = addr;
    fb.width = width;
    fb.height = height;
    fb.bytes_per_pixel = bpp;
    fb.stride = stride;
    fb.pixel_format = fmt;
    fb.row = 0;
    fb.col = 0;
    fb.fg = 0x0F;
    fb.bg = 0x00;
}

pub fn vga_clear(_fg: u8, _bg: u8) {
    let mut fb = FRAMEBUFFER.lock();
    fb.row = 0;
    fb.col = 0;
    if fb.addr == 0 { return; }
    let bg = fb.bg;
    for y in 0..fb.height {
        for x in 0..fb.width {
            let (r, g, b) = vga_color(bg);
            draw_pixel(&mut fb, x, y, r, g, b);
        }
    }
}

pub fn vga_write(s: &[u8], row: usize, fg: u8, bg: u8) {
    let mut fb = FRAMEBUFFER.lock();
    fb.fg = fg;
    fb.bg = bg;
    for (i, &b) in s.iter().enumerate().take(SCREEN_COLS) {
        draw_char(&mut fb, b, row, i);
    }
}

pub fn vga_putc(c: u8) {
    let (row, col) = {
        let fb = FRAMEBUFFER.lock();
        (fb.row, fb.col)
    };
    match c {
        b'\r' => { FRAMEBUFFER.lock().col = 0; }
        b'\n' => {
            let mut fb = FRAMEBUFFER.lock();
            fb.col = 0;
            if fb.row < SCREEN_ROWS - 1 { fb.row += 1; } else { scroll(&mut fb); }
        }
        0x08 => {
            let (row, col) = {
                let fb = FRAMEBUFFER.lock();
                (fb.row, fb.col)
            };
             if col > 0 {
                let mut fb = FRAMEBUFFER.lock();
                fb.col = col - 1;
                draw_char(&mut fb, b' ', row, col - 1);
            }
        }
        b if b >= 0x20 => {
            draw_char(&mut FRAMEBUFFER.lock(), c, row, col);
            let mut fb = FRAMEBUFFER.lock();
            fb.col += 1;
            if fb.col >= SCREEN_COLS {
                fb.col = 0;
                if fb.row < SCREEN_ROWS - 1 { fb.row += 1; } else { scroll(&mut fb); }
            }
        }
        _ => {}
    }
}

fn scroll(fb: &mut FramebufferState) {
    // Simple scroll: shift everything up by CHAR_HEIGHT pixels
    if fb.addr == 0 { return; }
    let shift = CHAR_HEIGHT;
    for y in shift..fb.height {
        for x in 0..fb.width {
            let (r, g, b) = {
                let bpp = fb.bytes_per_pixel;
                let off = y * fb.stride * bpp + x * bpp;
                unsafe {
                    let ptr = (fb.addr + off as u64) as *const u8;
                    (*ptr.add(2), *ptr.add(1), *ptr.add(0))
                }
            };
            draw_pixel(fb, x, y - shift, r, g, b);
        }
    }
    for y in (fb.height - shift)..fb.height {
        let bg = fb.bg;
        let (r, g, b) = vga_color(bg);
        for x in 0..fb.width {
            draw_pixel(fb, x, y, r, g, b);
        }
    }
}

pub fn outb(port: u16, val: u8) {
    unsafe { core::arch::asm!("out dx, al", in("dx") port, in("al") val); }
}

pub fn inb(port: u16) -> u8 {
    let val: u8;
    unsafe { core::arch::asm!("in al, dx", out("al") val, in("dx") port); }
    val
}

pub fn outw(port: u16, val: u16) {
    unsafe { core::arch::asm!("out dx, ax", in("dx") port, in("ax") val); }
}

pub fn inw(port: u16) -> u16 {
    let val: u16;
    unsafe { core::arch::asm!("in ax, dx", out("ax") val, in("dx") port); }
    val
}

pub fn reboot() -> ! {
    unsafe { core::arch::asm!("mov dx, 0xF4; mov al, 0x31; out dx, al", options(nostack, nomem)); }
    loop { unsafe { core::arch::asm!("hlt"); } }
}

pub fn debug_putc(c: u8) { outb(DEBUG_PORT, c); }

pub fn debug_write(s: &[u8]) {
    for &b in s { debug_putc(b); }
}

pub fn serial_init() {
    serial_init_port(COM1);
}

pub fn serial_init_port(port: u16) {
    outb(port + 1, 0x00); outb(port + 3, 0x80);
    outb(port + 0, 0x01); outb(port + 1, 0x00);
    outb(port + 3, 0x03); outb(port + 2, 0xC7);
    outb(port + 4, 0x0B);
}

pub fn serial_putc(c: u8) { serial_putc_port(COM1, c); }

pub fn serial_putc_port(port: u16, c: u8) {
    for _ in 0..10000 {
        if inb(port + 5) & 0x20 != 0 { outb(port, c); return; }
    }
}

pub fn serial_write(s: &[u8]) { serial_write_port(COM1, s); }

pub fn serial_write_port(port: u16, s: &[u8]) {
    for &b in s { serial_putc_port(port, b); }
}

#[allow(dead_code)]
pub fn serial_read_port(port: u16) -> Option<u8> {
    if inb(port + 5) & 1 != 0 { Some(inb(port)) } else { None }
}

pub fn console_write(s: &[u8]) {
    serial_write(s);
    for &b in s { vga_putc(b); }
}

pub fn console_putc(c: u8) {
    serial_putc(c);
    vga_putc(c);
}

static CPU_FREQ_KHZ: core::sync::atomic::AtomicU64 = core::sync::atomic::AtomicU64::new(0);

pub fn cpu_freq_khz() -> u64 {
    let cached = CPU_FREQ_KHZ.load(core::sync::atomic::Ordering::Relaxed);
    if cached != 0 { return cached; }
    // Calibrate by measuring RDTSC over a known serial write delay.
    let start = unsafe { core::arch::x86_64::_rdtsc() };
    for _ in 0..1000 { unsafe { core::arch::asm!("nop"); } }
    let end = unsafe { core::arch::x86_64::_rdtsc() };
    let ticks = end - start;
    // Rough: ~3 cycles per nop on modern CPUs, 1000 nops => 3000 cycles
    // But we're in QEMU, so just use a sane default.
    let freq = if ticks > 1000 { ticks * 2_000_000 / 1000 } else { 2_200_000 };
    CPU_FREQ_KHZ.store(freq, core::sync::atomic::Ordering::Relaxed);
    freq
}
