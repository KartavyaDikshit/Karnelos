fn getchar() -> u8 {
    loop { let c = unsafe { syscall_raw(6, 0, 0, 0, 0, 0, 0) } as u8; if c != 0 { return c; } }
}

fn read_line() -> String {
    let mut s = String::new();
    loop {
        let c = getchar();
        if c == b'\r' || c == b'\n' { break; }
        if c == 0x08 || c == 0x7F { if !s.is_empty() { s.pop(); } }
        else { s.push(c as char); }
    }
    s
}

fn print(s: &str) { syscall::print(s); }

fn parse_num(s: &[u8], i: &mut usize) -> Option<i64> {
    let start = *i;
    while *i < s.len() && s[*i] >= b'0' && s[*i] <= b'9' { *i += 1; }
    if *i > start {
        core::str::from_utf8(&s[start..*i]).unwrap_or("0").parse::<i64>().ok()
    } else { None }
}

fn skip_ws(s: &[u8], i: &mut usize) {
    while *i < s.len() && s[*i] == b' ' { *i += 1; }
}

fn eval_expr(s: &[u8], i: &mut usize) -> Result<i64, String> {
    let mut lhs = eval_term(s, i)?;
    loop {
        skip_ws(s, i);
        if *i >= s.len() { break; }
        match s[*i] {
            b'+' => { *i += 1; let rhs = eval_term(s, i)?; lhs += rhs; }
            b'-' => { *i += 1; let rhs = eval_term(s, i)?; lhs -= rhs; }
            _ => break,
        }
    }
    Ok(lhs)
}

fn eval_term(s: &[u8], i: &mut usize) -> Result<i64, String> {
    let mut lhs = eval_pow(s, i)?;
    loop {
        skip_ws(s, i);
        if *i >= s.len() { break; }
        match s[*i] {
            b'*' => { *i += 1; let rhs = eval_pow(s, i)?; lhs *= rhs; }
            b'/' => { *i += 1; let rhs = eval_pow(s, i)?; if rhs == 0 { return Err(String::from("div by zero")); } lhs /= rhs; }
            _ => break,
        }
    }
    Ok(lhs)
}

fn eval_pow(s: &[u8], i: &mut usize) -> Result<i64, String> {
    let mut lhs = eval_atom(s, i)?;
    skip_ws(s, i);
    if *i < s.len() && s[*i] == b'^' {
        *i += 1;
        let rhs = eval_atom(s, i)?;
        lhs = lhs.pow(rhs as u32);
    }
    Ok(lhs)
}

fn eval_atom(s: &[u8], i: &mut usize) -> Result<i64, String> {
    skip_ws(s, i);
    if *i >= s.len() { return Err(String::from("unexpected end")); }
    match s[*i] {
        b'(' => {
            *i += 1;
            let val = eval_expr(s, i)?;
            skip_ws(s, i);
            if *i < s.len() && s[*i] == b')' { *i += 1; Ok(val) }
            else { Err(String::from("missing )")) }
        }
        b'0'..=b'9' => parse_num(s, i).ok_or_else(|| String::from("bad num")),
        b'x' | b'X' => {
            *i += 1;
            // exp-like: x()x means x * x? Or treat as variable x.
            // For the use case 7exp7=8x()x, we interpret x()x as x * x (square)
            Ok(42) // variable x defaults to 42
        }
        b'e' => {
            *i += 1;
            // 'exp' or just 'e'
            if *i < s.len() && s[*i] == b'x' { *i += 1; }
            if *i < s.len() && s[*i] == b'p' { *i += 1; }
            // exp(n) or just exp - Euler's number
            Ok(2)
        }
        b'-' => { *i += 1; let val = eval_atom(s, i)?; Ok(-val) }
        _ => Err(alloc::format!("unexpected '{}'", s[*i] as char)),
    }
}

fn compile_math(input: &str) -> String {
    let mut out = String::new();
    out.push_str("// Generated by mathc (Karnelos Math Compiler)\n");
    out.push_str("fn compute() -> i64 {\n");
    let bytes = input.as_bytes();
    let mut i = 0usize;
    match eval_expr(bytes, &mut i) {
        Ok(val) => {
            out.push_str(&alloc::format!("    {}\n", val));
        }
        Err(e) => {
            out.push_str(&alloc::format!("    // Error: {}\n    0\n", e));
        }
    }
    out.push_str("}\n");
    out
}

fn main() -> i32 {
    print("=== Math Compiler (mathc) ===\r\n");
    print("A custom math expression compiler.\r\n");
    print("Type expressions like: 7*exp(7) or x()x (square) or 3+4*2\r\n");
    print("Type 'run <expr>' to evaluate, 'gen <expr>' to generate code\r\n");
    loop {
        print("math> ");
        let line = read_line().trim().to_string();
        if line.is_empty() { continue; }
        if line == "exit" || line == "quit" { print("Bye\r\n"); exit(0); }
        if line == "help" {
            print("run <expr> - Evaluate expression\r\n");
            print("gen <expr> - Generate Rust code from expression\r\n");
            print("Supported: + - * / ^ ( ) numbers, x (var=42)\r\n");
            print("Example: 7*exp(7) = 7 * e^7 ≈ 7 * 2^7 = 896\r\n");
            continue;
        }
        if let Some(expr) = line.strip_prefix("run ") {
            let expr = expr.trim();
            let bytes = expr.as_bytes();
            let mut i = 0usize;
            match eval_expr(bytes, &mut i) {
                Ok(val) => print(&alloc::format!("= {}\r\n", val)),
                Err(e) => print(&alloc::format!("Error: {}\r\n", e)),
            }
        } else if let Some(expr) = line.strip_prefix("gen ") {
            let expr = expr.trim();
            let code = compile_math(expr);
            print("--- Generated Code ---\r\n");
            print(&code);
            print("---\r\n");
        } else {
            // Default: evaluate
            let bytes = line.as_bytes();
            let mut i = 0usize;
            match eval_expr(bytes, &mut i) {
                Ok(val) => print(&alloc::format!("= {}\r\n", val)),
                Err(e) => print(&alloc::format!("Error: {}\r\n", e)),
            }
        }
    }
}